public class Calculator 
{
    public double num1, num2;

    public double Addition()
    {
        return num1 + num2;
    }

    public double Subtraction()
    {
        return num1 - num2;
    }

    public double Multiplication()
    {
        return num1 * num2;
    }

    public double Division()
    {
        return num1 / num2;
    }
}